#define			WATCHDATA_ID_SECOND				(WATCHDATA_ID_BASE + 4)
#define			WATCHDATA_ID_24H				(WATCHDATA_ID_BASE + 5)






#endif	/* _RENOWATCHDATATYPE_H_ */